int	count_hash(char *str)
{
	int count;

	count = 0;
	if (*str == '\n')
		return (0);
	while (*str != '\0')
	{
		if (*str != '#' && *str != '.')
			return (-1);
		if (*str == '#')
			count++;
		str++;
	}
	return (count);
}
