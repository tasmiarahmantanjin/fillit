#include "libft.h"

void	*ft_memset(void *str, int c, size_t len)
{
	int		i;
	char	*ptr;

	ptr = str;
	i = 0;
	while (len > 0)
	{
		ptr[i] = c;
		i++;
		len--;
	}
	return (str);
}
