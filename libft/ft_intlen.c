#include "libft.h"

int		ft_intlen(int n)
{
	int		len;
	long	nb;

	nb = n;
	len = 0;
	if (nb < 0)
	{
		nb = nb * -1;
		len++;
	}
	if (nb == 0)
		len++;
	while (nb > 0)
	{
		nb = nb / 10;
		len++;
	}
	return (len);
}
